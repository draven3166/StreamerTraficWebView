package com.erbol.bo.DataModel;

public class Figures {

  private long idc;
  private String latp;
  private String lngp;
  private long radc;
  private String fillc;
  private String stroke;
  private String typef;

  public long getIdc() {
    return idc;
  }

  public void setIdc(long idc) {
    this.idc = idc;
  }

  public String getLatp() {
    return latp;
  }

  public void setLatp(String latp) {
    this.latp = latp;
  }

  public String getLngp() {
    return lngp;
  }

  public void setLngp(String lngp) {
    this.lngp = lngp;
  }

  public long getRadc() {
    return radc;
  }

  public void setRadc(long radc) {
    this.radc = radc;
  }

  public String getFillc() {
    return fillc;
  }

  public void setFillc(String fillc) {
    this.fillc = fillc;
  }

  public String getStroke() {
    return stroke;
  }

  public void setStroke(String stroke) {
    this.stroke = stroke;
  }

  public String getTypef() {
    return typef;
  }

  public void setTypef(String typef) {
    this.typef = typef;
  }
}